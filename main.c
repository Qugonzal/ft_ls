#include "ft_ls.h"

int		main(int argc, char **argv)
{
	int i;

	i = 1;
	if (argc == 1)
		ft_ls(".");
	if (argc > 1)
	{
		while (i < argc)
		{
			ft_ls(argv[i]);
			i++;
		}
	}
	return (0);
}
