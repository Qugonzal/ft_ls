#include "libft.h"

char	*ft_strstr(const char *big, const char *little)
{
	int i;

	i = 0;
	if (!little[i])
		return ((char *)big);
	while (big[i])
	{
		if (!(ft_strncmp(&big[i], little, ft_strlen(little))))
			return ((char *)&big[i]);
		i++;
	}
	return (0);
}
